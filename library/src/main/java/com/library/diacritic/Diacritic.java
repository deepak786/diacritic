    /**
     * Removes accents and diacritics from the given String.
     */
    public static String removeDiacritics(String text) {
        if (text == null || text.length() == 0 || text.trim().length() == 0) {
            // invalid text
            return text;
        }

        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < text.length(); i++) {
            int codePoint = text.codePointAt(i);
            // Combining Diacritical Marks in Unicode
            if (codePoint >= 0x0300 && codePoint <= 0x036F) {
                continue;
            }

            String replacedChar = map.get(codePoint);
            if (replacedChar != null) {
                // append the replacedChar
                sb.append(replacedChar);
                continue;
            }

            // no need to replace this char so append as it is
            sb.append(text.charAt(i));
        }

        return sb.toString();
    }
